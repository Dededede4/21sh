#include <string.h>

void	*ft_memccpy(void *dst, const void *src, int c, size_t n)
{
	size_t	i;

	i = 0;
	while (i < n)
	{
		((char *)dst)[i] = ((char *)src)[i];
		++i;
		if (((unsigned char *)dst)[i - 1] == (unsigned char)c)
			return (dst + i);
	}
	return (NULL);
}
